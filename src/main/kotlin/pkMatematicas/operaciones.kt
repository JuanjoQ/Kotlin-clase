package pkMatematicas

fun operacionSuma(num1: Int, num2: Int) {
    return println(num1+num2)

}

fun operacionResta(num1: Int, num2: Int) {
    return println(num1-num2)

}


fun operacionMult(num1: Int, num2: Int) {
    return println(num1*num2)

}


fun operacionDiv(num1: Int, num2: Int) {
    return println(num1/num2)

}